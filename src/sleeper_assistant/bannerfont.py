_BLANK = ["   "] * _GLYPH_H


def banner(text: str) -> str:
    """Render `text` as multi-line block-font art. Newlines stack words with a
    blank spacer line between them; unknown characters render as blank space."""
    blocks: list[str] = []
    for line in text.upper().split("\n"):
        rows = [""] * _GLYPH_H
        for ch in line:
            glyph = _GLYPHS.get(ch, _BLANK)
            for i in range(_GLYPH_H):
                rows[i] += glyph[i]
        blocks.append("\n".join(rows))
    return "\n\n".join(blocks)
